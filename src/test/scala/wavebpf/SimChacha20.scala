package wavebpf

import spinal.core._
import spinal.sim._
import spinal.core.sim._
import scala.util.Random

import org.scalatest.funsuite.AnyFunSuite

class SimChacha20Spec extends AnyFunSuite {
  // tests go here...
  test("SimChacha20") {
    import SimUtil._
    val config = DefaultWbpfConfig()
    runWithAllConfig { dut =>
      initDutForTesting(dut)
      val firstExc = dut.io.excOutput.head

      loadCode(
        dut,
        0,
        0x0,
        Chacha20ObjectCode()
      )
      println("Code loaded.")

      val key = new Array[Byte](32)
      Random.nextBytes(key)
      val nonce = new Array[Byte](12)
      Random.nextBytes(nonce)
      val data = new Array[Byte](100)
      Random.nextBytes(data)

      var addr = 0x100
      dmWriteOnce(dut, 0x10, addr)
      dmWriteOnce(dut, 0x18, data.length)
      dmWriteBytesAxi4(dut, addr, data)
      addr += data.length
      dmWriteOnce(dut, 0x20, addr)
      dmWriteBytesAxi4(dut, addr, key)
      addr += key.length
      dmWriteOnce(dut, 0x28, addr)
      dmWriteBytesAxi4(dut, addr, nonce)
      addr += nonce.length
      dmWriteOnce(dut, 0x30, 0)
      println("Data loaded.")

      assert(firstExc.valid.toBoolean)
      assert(firstExc.code.toEnum == CpuExceptionCode.NOT_INIT)

      mmioWrite(dut, 0x1018, 0x00)
      resetPerfCounters(dut, 0)
      dut.clockDomain.waitSamplingWhere(!firstExc.valid.toBoolean)
      var cycleCount = 0
      while (!firstExc.valid.toBoolean) {
        cycleCount += 1
        dut.clockDomain.waitSampling()
      }
      assert(firstExc.code.toEnum == CpuExceptionCode.EXIT)

      println("Cycles: " + cycleCount)
      printPerfCounters(dut, 0)
      val chacha20 = new wavebpf.testutil.ChaCha20(key, nonce, 0)
      val expected = new Array[Byte](data.length)
      chacha20.encrypt(expected, data, data.length)
      val actual = (0 until data.length).map(i =>
        dmReadOnce(dut, 0x100 + i, MemoryAccessWidth.W1).toByte
      )
      assert(expected.toSeq == actual)
      println("Validation OK")
    }
  }
}

